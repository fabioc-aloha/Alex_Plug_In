/**
 * Convert SVG string to base64 data URI for embedding.
 * Optionally applies a color tint to the icon.
 */
export function svgToDataUri(svg: string, color?: string): string {
    let processedSvg = svg;
    
    // Apply color tint if specified
    if (color) {
        // Replace currentColor and stroke="currentColor" with the specified color
        processedSvg = svg
            .replace(/stroke="currentColor"/g, `stroke="#${color}"`)
            .replace(/fill="currentColor"/g, `fill="#${color}"`);
    }
    
    // Encode for data URI
    const encoded = Buffer.from(processedSvg).toString('base64');
    return `data:image/svg+xml;base64,${encoded}`;
}

