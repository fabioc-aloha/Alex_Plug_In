#!/usr/bin/env node
/**
 * gen-model-tool-matrix.cjs
 *
 * Auto-generates model capability and tool compatibility matrices from source code.
 * Reads:
 *   - modelIntelligence.ts -> MODEL_FAMILY_PATTERNS, COGNITIVE_TASKS
 *   - cognitiveTier.ts -> COGNITIVE_TIERS, FEATURE_REQUIREMENTS
 *   - package.json -> languageModelTools declarations
 *   - tools/index.ts + globalKnowledgeTools.ts -> registerTool calls
 *
 * Output: alex_docs/MODEL-TOOL-MATRIX.md
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const EXT_ROOT = path.join(ROOT, 'platforms', 'vscode-extension');
const SRC = path.join(EXT_ROOT, 'src');
const OUTPUT = path.join(ROOT, 'alex_docs', 'MODEL-TOOL-MATRIX.md');

// --- Parsers ------------------------------------------------------------------

function parseModelFamilyPatterns() {
  const file = path.join(SRC, 'chat', 'modelIntelligence.ts');
  const content = fs.readFileSync(file, 'utf8');

  // Extract the MODEL_FAMILY_PATTERNS array
  const blockMatch = content.match(/MODEL_FAMILY_PATTERNS[\s\S]*?=\s*\[([\s\S]*?)\];/);
  if (!blockMatch) return [];

  const entries = [];
  const regex = /pattern:\s*\/(.*?)\/\w*,\s*tier:\s*'(\w+)'/g;
  let m;
  while ((m = regex.exec(blockMatch[1])) !== null) {
    entries.push({ pattern: m[1], tier: m[2] });
  }
  return entries;
}

function parseCognitiveTasks() {
  const file = path.join(SRC, 'chat', 'modelIntelligence.ts');
  const content = fs.readFileSync(file, 'utf8');

  const blockMatch = content.match(/COGNITIVE_TASKS[\s\S]*?=\s*\{([\s\S]*?)\n\};/);
  if (!blockMatch) return [];

  const tasks = [];
  const regex = /(\w+):\s*\{[^}]*name:\s*'([^']+)'[^}]*minimumTier:\s*'(\w+)'[^}]*description:\s*'([^']+)'/g;
  let m;
  while ((m = regex.exec(blockMatch[1])) !== null) {
    tasks.push({ id: m[1], name: m[2], minimumTier: m[3], description: m[4] });
  }
  return tasks;
}

function parseToolDeclarations() {
  const pkgPath = path.join(EXT_ROOT, 'package.json');
  const pkg = JSON.parse(fs.readFileSync(pkgPath, 'utf8'));
  const tools = pkg.contributes?.languageModelTools || [];
  return tools.map(t => ({
    name: t.name,
    displayName: t.displayName,
    refName: t.toolReferenceName,
    description: t.userDescription || t.modelDescription?.substring(0, 80) + '...',
    tags: t.tags || [],
    readOnly: t.annotations?.readOnlyHint || false,
  }));
}

function parseFeatureRequirements() {
  const file = path.join(SRC, 'shared', 'cognitiveTier.ts');
  const content = fs.readFileSync(file, 'utf8');

  const blockMatch = content.match(/FEATURE_REQUIREMENTS[\s\S]*?=\s*\{([\s\S]*?)\n\};/);
  if (!blockMatch) return [];

  const features = [];
  const regex = /'([^']+)':\s*\{[^}]*featureName:\s*'([^']+)'[^}]*minimumLevel:\s*(\d)[^}]*description:\s*'([^']+)'/g;
  let m;
  while ((m = regex.exec(blockMatch[1])) !== null) {
    features.push({ command: m[1], name: m[2], level: parseInt(m[3]), description: m[4] });
  }
  return features;
}

// --- Generator ----------------------------------------------------------------

function generateMarkdown() {
  const models = parseModelFamilyPatterns();
  const tasks = parseCognitiveTasks();
  const tools = parseToolDeclarations();
  const features = parseFeatureRequirements();

  const now = new Date().toISOString().split('T')[0];

  let md = `# Model & Tool Capability Matrix

> Auto-generated by \`scripts/gen-model-tool-matrix.cjs\` -- do not edit manually.
> Last generated: ${now}

---

## Model Tier Classification

Models are classified into three operational tiers based on reasoning depth and context window.

| Tier | Models | Capabilities |
| --- | --- | --- |
| **Frontier** | ${models.filter(m => m.tier === 'frontier').map(m => '`' + m.pattern.replace(/\\/g, '') + '`').join(', ')} | Deep reasoning, 1M+ context, extended thinking |
| **Capable** | ${models.filter(m => m.tier === 'capable').map(m => '`' + m.pattern.replace(/\\/g, '') + '`').join(', ')} | Good reasoning, 200K-400K context |
| **Efficient** | ${models.filter(m => m.tier === 'efficient').map(m => '`' + m.pattern.replace(/\\/g, '') + '`').join(', ')} | Fast, cost-effective, limited reasoning |

---

## Cognitive Task -> Minimum Tier

Each cognitive task has a minimum model tier for reliable results.

| Task | Minimum Tier | Description |
| --- | --- | --- |
`;

  for (const t of tasks) {
    const tierIcon = t.minimumTier === 'frontier' ? '[BRAIN]' : t.minimumTier === 'capable' ? '[BOT]' : '[!]';
    md += `| ${t.name} | ${tierIcon} ${t.minimumTier} | ${t.description} |\n`;
  }

  md += `
---

## Feature -> Cognitive Level Requirements

Commands require specific cognitive levels (1=Minimum, 2=Basic, 3=Recommended, 4=Advanced).

| Command | Feature | Level | Description |
| --- | --- | --- | --- |
`;

  for (const f of features) {
    const levelIcon = f.level === 4 ? '[BRAIN]' : f.level === 3 ? '[BOT]' : f.level === 2 ? '[MSG]' : '[DIR]';
    md += `| \`${f.command}\` | ${f.name} | ${levelIcon} L${f.level} | ${f.description} |\n`;
  }

  md += `
---

## Language Model Tools

Tools registered via \`vscode.lm.registerTool\` and available to Copilot agent mode.

| Tool ID | Display Name | Tags | Read-Only |
| --- | --- | --- | --- |
`;

  for (const t of tools) {
    md += `| \`${t.name}\` | ${t.displayName} | ${t.tags.join(', ')} | ${t.readOnly ? '[OK]' : '[X]'} |\n`;
  }

  md += `
---

## MCP Server Tools

Tools exposed via the standalone MCP server (\`packages/mcp-cognitive-tools/\`).

| Tool | Description |
| --- | --- |
| \`alex_synapse_health\` | Validate cognitive architecture integrity |
| \`alex_memory_search\` | Search all memory systems |
| \`alex_architecture_status\` | Current architecture inventory |
| \`alex_knowledge_search\` | Search global knowledge base |
| \`alex_knowledge_save\` | Save insights to global knowledge |

---

*Source: \`platforms/vscode-extension/src/chat/modelIntelligence.ts\`, \`src/shared/cognitiveTier.ts\`, \`package.json\`, \`packages/mcp-cognitive-tools/\`*
`;

  return md;
}

// --- Main ---------------------------------------------------------------------

function main() {
  const md = generateMarkdown();
  fs.writeFileSync(OUTPUT, md, 'utf8');
  console.log(`[gen-model-tool-matrix] [OK] Generated ${path.relative(ROOT, OUTPUT)}`);
  console.log(`  Models: ${parseModelFamilyPatterns().length} patterns`);
  console.log(`  Tasks: ${parseCognitiveTasks().length} cognitive tasks`);
  console.log(`  Tools: ${parseToolDeclarations().length} LM tools`);
  console.log(`  Features: ${parseFeatureRequirements().length} feature requirements`);
}

main();
